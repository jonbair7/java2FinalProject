    private void btnLogSummaryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogSummaryActionPerformed
        try
        {
        model.addElement("");
        
        String pt = txtPassThresh.getText();
        String fp1 = txtFinalPercent1.getText();
        String fp2 = txtFinalPercent2.getText();
        String fp3 = txtFinalPercent3.getText();
        String fp4 = txtFinalPercent4.getText();
        String fp5 = txtFinalPercent5.getText();
        
        if(pt.equals(""))
        {
            txtPassThresh.setEditable(true);
            txtPassThresh.setBackground(Color.white);
            txtPassThresh.requestFocusInWindow();
            JOptionPane.showMessageDialog(null, "Error: box is empty");
            return;
        }
        
        Double ptv = Double.valueOf(pt);
        Double fpv1 = Double.valueOf(fp1);
        Double fpv2 = Double.valueOf(fp2);
        Double fpv3 = Double.valueOf(fp3);
        Double fpv4 = Double.valueOf(fp4);
        Double fpv5 = Double.valueOf(fp5);
        
        if(ptv >= 0 && ptv <= 100){}
            else
            {
                txtPassThresh.setText("");
                txtPassThresh.requestFocusInWindow();
                JOptionPane.showMessageDialog(null, "Error: enter a valid number for pass threshold");
                return;
            }
        
        int pass = 0, fail = 0;
        
        if(fpv1 >= ptv)
            pass++;
        else
            fail++;
        
        if(fpv2 >= ptv)
            pass++;
        else
            fail++;
        
        if(fpv3 >= ptv)
            pass++;
        else
            fail++;
        
        if(fpv4 >= ptv)
            pass++;
        else
            fail++;
        
        if(fpv5 >= ptv)
            pass++;
        else
            fail++;
        
        model.addElement("PASS: " + pass + "     " + "FAIL: " + fail);
        }
        catch(NumberFormatException n)
        {
            txtPassThresh.setText("");
            txtPassThresh.requestFocusInWindow();
            JOptionPane.showMessageDialog(null, "Error: enter a valid number for pass threshold");
        }
    }//GEN-LAST:event_btnLogSummaryActionPerformed

