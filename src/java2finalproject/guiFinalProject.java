    private void btnSubmit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmit1ActionPerformed
        try
        {
            String fn1 = txtFName1.getText();
            String ln1 = txtLName1.getText();
            String fp1 = txtFinalPercent1.getText();
            Double fpv1 = Double.valueOf(fp1);
            
            if(fn1.equals(""))
            {
                txtFName1.requestFocusInWindow();
                JOptionPane.showMessageDialog(null, "Error: box is empty");
                return;
            }
            
            if(ln1.equals(""))
            {
                txtLName1.requestFocusInWindow();
                JOptionPane.showMessageDialog(null, "Error: box is empty");
                return;
            }
            
            if(fpv1 >= 0 && fpv1 <= 100){}
            else
            {
                txtFinalPercent1.setText("");
                txtFinalPercent1.requestFocusInWindow();
                JOptionPane.showMessageDialog(null, "Error: enter a valid number");
                return;
            }
                
            student student1 = new student(fpv1);
            student1.setFname(fn1);
            student1.setLname(ln1);
            student1.setGrade(fp1);
            
            String Fname = student1.getFname();
            String Lname = student1.getLname();
            String Percent = String.valueOf(student1.getPercentage());
            String Grade = student1.getGrade();
            
            model.addElement(Lname + ", " + Fname + " " + Percent + " - " + Grade);
            
            txtFName1.setEditable(false);
            txtFName1.setBackground(Color.gray);
            txtLName1.setEditable(false);
            txtLName1.setBackground(Color.gray);
            txtFinalPercent1.setEditable(false);
            txtFinalPercent1.setBackground(Color.gray);
            btnSubmit1.setEnabled(false);
        }
        catch(InputMismatchException a) {}
        catch(NumberFormatException n)
        {
            txtFinalPercent1.setText("");
            txtFinalPercent1.requestFocusInWindow();
            JOptionPane.showMessageDialog(null, "Error: enter a valid number");
        }
        
        if(btnSubmit1.isEnabled() == false &&
           btnSubmit2.isEnabled() == false &&
           btnSubmit3.isEnabled() == false &&
           btnSubmit4.isEnabled() == false &&
           btnSubmit5.isEnabled() == false)
        {
            btnLogSave.setEnabled(true);
            btnLogLoad.setEnabled(false);
            btnLogSummary.setEnabled(true);
            txtPassThresh.setEditable(false);
            txtPassThresh.setBackground(Color.gray);
        }
    }//GEN-LAST:event_btnSubmit1ActionPerformed

